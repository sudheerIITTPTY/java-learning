public class UnaryOperatorsOnIntegers {
    public static void main(String[] args){
        // declaring integer with initial value as 10
        int num = 10;

        //printing number
        System.out.println("Number : " +num);

        // printing num value while updating using both post as well as pre increment
        System.out.println("printing while post increment the number : " + num++);
        System.out.println("printing while pre increment value of num " + ++num);
    }
}
